    def set_language(self, language):
        """Set the current language and save to config"""
        if self.current_language != language:
            self.current_language = language
            if self.config_manager:
                self.config_manager.update_setting("general_settings.display_language", language)
                self.config_manager.save_config()
            self.language_changed.emit(language)
    
    def get_language(self):
        """Get the current language"""
        if self.config_manager:
            self.current_language = self.config_manager.get("general_settings.display_language", "English")
        return self.current_language
    
    def t(self, key):
        """Translate a key to the current language"""
        lang = self.get_language()
        if lang in self.translations and key in self.translations[lang]:
            return self.translations[lang][key]
        # Fallback to English if key not found in current language
        if key in self.translations["English"]:
            return self.translations["English"][key]
        # Return the key itself if no translation found at all
        return key